// Open File
function openFile() {
  const files = dialog.showOpenDialogSync(mainWindow, {
    properties: ["openFile"],
    filters: [{ name: "Markdown", extensions: ["md", "markdown", "txt"] }],
  });
  console.log(files[0]);
  if (!files) return;

  const file = files[0];

  console.log("File Content...")
  const fileContent = fs.readFileSync(file).toString();
  console.log(fileContent);
}
